#include<iostream>
using namespace std;
int main() {
    // ==, <, >, <=, >=, !=
    
    int a = 3;
    int b = 5;

    cout<<(a == b)<<endl; //flase
    cout<<(a != b)<<endl; //true

    cout<<(a > b)<<endl; //false
    cout<<(a < b)<<endl; //true

    cout<<(a >= b)<<endl; //flase
    cout<<(a <= b)<<endl; //true

}