#include<iostream>
using namespace std;
int main() {
    int a;
    cout<<"address of a : "<<&a<<endl;

    char ch;
    cout<<"address of ch : "<<&ch<<endl;

    float f;
    cout<<"address of float : "<<&f<<endl;

    bool b;
    cout<<"address of bool : "<<&b<<endl;

    double d;
    cout<<"address of double : "<<&d<<endl;
}